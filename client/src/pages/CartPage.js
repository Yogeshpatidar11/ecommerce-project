  const handlePayment = async () => {
    if (!auth?.token) {
      toast.error("Please login to continue ‚ùå");
      navigate("/login", { state: "/cart" }); // login hone ke baad wapas cart me aa jaye
      return;
    }

    try {
      setLoading(true);

      // üü¢ Step 1: Create Order from backend
      const { data } = await axios.post("/api/v1/product/razorpay/order", {
        amount: totalAmount,
      });

      // üü¢ Step 2: Razorpay Checkout
      const options = {
        key: data.key,
        order_id: data.order.id,
        amount: data.order.amount,
        currency: data.order.currency,
        name: "My Grocery Store",
        description: "Test Transaction",
        handler: async function (response) {
          try {
            const verifyRes = await axios.post(
              "/api/v1/product/razorpay/verify",
              {
                razorpay_payment_id: response.razorpay_payment_id,
                razorpay_order_id: response.razorpay_order_id,
                razorpay_signature: response.razorpay_signature,
                cart,
                buyer: user._id,
                amount: totalAmount,
              }
            );

            if (verifyRes.data.success) {
              toast.success("Payment successful üéâ");

              // üü¢ Clear Cart (context + localStorage)
              setCart([]);
              localStorage.removeItem("cart");

              // Redirect to orders page
              navigate("/dashboard/user/orders");
            } else {
              toast.error("Payment verification failed ‚ùå");
            }
          } catch (error) {
            console.error(error);
            toast.error("Payment verification failed ‚ùå");
          }
        },
        prefill: {
          name: auth?.user?.name,
          email: auth?.user?.email,
          contact: "9999999999",
        },
        theme: { color: "#3399cc" },
      };

      const rzp = new window.Razorpay(options);
      rzp.open();
      setLoading(false);
    } catch (err) {
      console.error(err);
      setLoading(false);
    }
  };

